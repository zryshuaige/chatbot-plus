# 自定义任务的缺省提示词
CUSTOM_DEFAULT_PROMPT = "你是一个有用的助手，请根据用户需求给出清晰、准确的回答。"


def get_prompt(task: Optional[str]) -> str:
    """根据任务代号取系统提示词；找不到则回退到自定义默认。"""
    if task and task in TASK_PROMPTS:
        return TASK_PROMPTS[task]["prompt"]
    return CUSTOM_DEFAULT_PROMPT


def tasks_summary() -> list[dict]:
    """给前端展示用的任务清单（不含完整提示词正文，减少传输）。
    图片类任务额外带 templates（提示词模版）供前端展示卡片。"""
    out = []
    for k, v in TASK_PROMPTS.items():
        item = {"key": k, "name": v["name"], "icon": v["icon"]}
        if "model" in v:
            item["model"] = v["model"]  # 前端据此判断是否图片任务
        if v.get("templates"):
            item["templates"] = v["templates"]
        out.append(item)
    return out
